;输入a输出str1
;输入b输出str2
;输入c输出str3


DSEG SEGMENT
	
	FUNCTABLE	DW	FUNC1, FUNC2, FUNC3  ;DW存放偏移地址
	
	STR1		DB	"111111111", '$'
	STR2		DB	"222222222", '$'
	STR3		DB	"333333333", '$'
	
DSEG ENDS


SSEG SEGMENT STACK

SSEG ENDS

CSEG SEGMENT
	ASSUME CS:CSEG, DS:DSEG, SS:SSEG
START:
	MOV AX, DSEG
	MOV DS, AX 
	
	MOV AX, SSEG
	MOV SS, AX 
	
	MOV AH, 1
	INT 21H   		;读取键盘放在AL中
	OR	AL, 20H		;变小写
	SUB AL, 'a'
	MOV	BL, AL
	XOR BH, BH
	
	JMP FUNCTABLE[BX]

FUNC1:
	MOV DX, OFFSET STR1
	MOV AH, 9
	INT 21H
	JMP EXIT
	
FUNC2:
	MOV DX, OFFSET STR2
	MOV AH, 9
	INT 21H
	JMP EXIT
	
FUNC3:
	MOV DX, OFFSET STR3
	MOV AH, 9
	INT 21H
	JMP EXIT
	
EXIT:
	MOV AX, 4C00H
	INT 21H

CSEG ENDS
END START