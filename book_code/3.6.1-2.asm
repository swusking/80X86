;不用乘法指令实现乘法运算
;移位相加

DSEG SEGMENT
	XXX	DB	234 ;被乘数
	YYY	DB	125	;乘数
	ZZZ	DW	?	;积
DSEG ENDS

SSEG SEGMENT STACK

SSEG ENDS

CSEG SEGMENT
	ASSUME CS:CSEG, DS:DSEG, SS:SSEG
START:
	MOV AX, DSEG
	MOV DS, AX 
	
	MOV AX, SSEG
	MOV SS, AX 
	
	MOV AL, XXX
	XOR AH, AH
	MOV BL, YYY  
	XOR DX, DX		;累加器
	
	MOV CX, 8		;最多移位8次
S:	
	SHR	BL, 1
	JNC	NEXT
	ADD DX, AX 
NEXT:
	ADD AX, AX
	LOOP S
	
	MOV ZZZ, DX
	
	MOV AX, 4C00H
	INT 21H

CSEG ENDS
END START