;16进制数对应的七段代码数码管
;0亮，1灭，共阳极

DSEG SEGMENT
	TABLE	DB	1000000B, 1111001B, 0100100B, 0110000B
			DB	0011001B, 0010010B,	0000010B, 1111000B
			DB	0000000B, 0010000B, 0001000B, 0000011B
			DB 	1000110B, 0100001B, 0000110B, 0001110B
	
	XCODE	DB	8	;16进制数字
	YCODE	DB	?	;存放结果
DSEG ENDS


SSEG SEGMENT STACK

SSEG ENDS

CSEG SEGMENT
	ASSUME CS:CSEG, DS:DSEG, SS:SSEG
START:
	MOV AX, DSEG
	MOV DS, AX 
	
	MOV AX, SSEG
	MOV SS, AX 
	
	
	MOV BX, OFFSET TABLE
	MOV AL, XCODE 
	XLAT
	MOV YCODE, AL 
	
	
	MOV AX, 4C00H
	INT 21H

CSEG ENDS
END START