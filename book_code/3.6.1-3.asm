;16位二进制数转化为5位十进制数
;16位二进制数最大能表示65535，所以5位足够了


DSEG SEGMENT
	DATA	DW	23456
	RESULT	DB	5 DUP(?)
DSEG ENDS

SSEG SEGMENT STACK

SSEG ENDS

CSEG SEGMENT
	ASSUME CS:CSEG, DS:DSEG, SS:SSEG
START:
	MOV AX, DSEG
	MOV DS, AX 
	
	MOV AX, SSEG
	MOV SS, AX 
	
	MOV SI, OFFSET RESULT
	ADD SI, 4
	MOV AX, DATA
	XOR DX, DX
	MOV BX, 10
S:
	DIV BX 
	MOV [SI], DL
	CMP AX, 0
	JZ	EXIT 		;被除数=0则跳出
	XOR DX, DX
	DEC SI
	JMP	S
	
EXIT:
	MOV AX, 4C00H
	INT 21H

CSEG ENDS
END START