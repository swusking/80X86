;判断0040:0000开始的2048个字节内存单元是否有字符'A'
;如果有则把存储单元偏移地址送入0000:03FEH

SSEG SEGMENT STACK

SSEG ENDS

CSEG SEGMENT
	ASSUME	CS:CSEG, SS:SSEG
START:
	MOV	AX, SSEG
	MOV SS, AX 
	MOV	AX, 0040H
	MOV DS, AX
	MOV AX, 0000H
	MOV ES, AX 
	
	MOV SI, 0
	MOV DI, 03FEH
	MOV CX, 2048
S:
	MOV AL, [SI]
	MOV WORD PTR ES:[DI], SI
	
	CMP AL, 'A'
	JZ	AGAIN
	MOV WORD PTR ES:[DI], 0FFFFH
	
AGAIN:
	ADD DI, 2
	INC SI 
	LOOP S
	
EXIT:
	MOV AX, 4C00H
	INT 21H

CSEG ENDS
END START