;压缩BCD码转换为ASCII码

DSEG SEGMENT
	BCD 	DB	86H
	ASCII	DB	2 dup(0)
DSEG ENDS


SSEG SEGMENT STACK

SSEG ENDS

CSEG SEGMENT
	ASSUME CS:CSEG, DS:DSEG, SS:SSEG
START:
	MOV AX, DSEG
	MOV DS, AX 
	
	MOV AX, SSEG
	MOV SS, AX 
	
	MOV AL, BCD 
	MOV AH, AL
	AND AX, 0F00FH; AH=80, AL=06
	
	MOV CL, 4
	ROR AH, CL ; AH=08, AL=06
	
	ADD AH, 30H
	ADD AL, 30H
	MOV ASCII, AH
	MOV ASCII+1, AL
	
	MOV AX, 4C00H
	INT 21H

CSEG ENDS
END START