;计算内存从地址0040:0000H开始的1024个字的字校验和

DSEG SEGMENT
	CHECKSUM	DW	?
DSEG ENDS


SSEG SEGMENT STACK

SSEG ENDS

CSEG SEGMENT
	ASSUME CS:CSEG, DS:DSEG, SS:SSEG
START:
	MOV AX, DSEG
	MOV DS, AX 
	
	MOV AX, SSEG
	MOV SS, AX 
	
	MOV AX, 0040H
	MOV ES, AX 
	MOV BX, 0
	MOV CX, 1024
	XOR AX, AX 
S:	
	ADD AX, ES:[BX]
	ADC AX, 0
	INC BX
	INC BX
	LOOP S
	
	MOV [BX], AX 
	
	MOV AX, 4C00H
	INT 21H

CSEG ENDS
END START