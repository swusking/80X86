;冒泡排序，有小到大，单字节无符号整数
;每次把最大的挤到最后后面去
;采用基址寻址方式

COUNT = 10

DSEG SEGMENT
	DATA	DB	23H, 12H, 45H, 27H, 3H, 9H, 58H, 81H, 0H, 72H
DSEG ENDS

SSEG SEGMENT STACK

SSEG ENDS

CSEG SEGMENT
	ASSUME	CS:CSEG, DS:DSEG, SS:SSEG
START:
	MOV AX, DSEG
	MOV DS, AX
	MOV	AX, SSEG
	MOV SS, AX 
	
	XOR SI, SI 
	MOV CX, COUNT - 1 ;外层循环
	
LOOP1:
	PUSH CX
LOOP2:
	MOV AL, DATA[SI]
	CMP AL, DATA[SI+1]
	JB	TRAD
	XCHG AL, DATA[SI+1]
TRAD:
	MOV DATA[SI], AL
	INC SI
	LOOP LOOP2
	
	POP	CX
	XOR SI, SI
	LOOP LOOP1
	
	
EXIT:
	MOV AX, 4C00H
	INT 21H

CSEG ENDS
END START